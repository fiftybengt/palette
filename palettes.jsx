// helpers — kept local, exported via window
const rgbStr = (t) => `rgb(${t[0]} ${t[1]} ${t[2]})`;
const rgbAlpha = (t, a) => `rgb(${t[0]} ${t[1]} ${t[2]} / ${a})`;
const hexOf = (t) =>
  "#" +
  t
    .map((n) => n.toString(16).padStart(2, "0").toUpperCase())
    .join("");

// tokens object → CSS custom-property style block
function paletteToVars(tokens) {
  const out = {};
  out["--c-bg"] = rgbStr(tokens.bg);
  out["--c-surface"] = rgbStr(tokens.surface);
  out["--c-raised"] = rgbStr(tokens.raised);
  out["--c-sunken"] = rgbStr(tokens.sunken);
  out["--c-line"] = rgbStr(tokens.line);
  out["--c-line-strong"] = rgbStr(tokens.lineStrong);
  out["--c-ink"] = rgbStr(tokens.ink);
  out["--c-muted"] = rgbStr(tokens.muted);
  out["--c-soft"] = rgbStr(tokens.soft);
  out["--c-accent"] = rgbStr(tokens.accent);
  out["--c-accent-strong"] = rgbStr(tokens.accentStrong);
  out["--c-accent-ink"] = rgbStr(tokens.accentInk);
  return out;
}

